<h2><a name="piano"></a>Piano Of Life</h2>
<p>I rest my hands on white keys<br/>
  A piano sits in front of me<br/>
  I began to play a song or two<br/>
  From a Hymn Book in my view</p>
<p>The Grand Piano's made of oak<br/>
  I think to myself "Will it float"?<br/>
  The song continues and with time<br/>
  My Orchestra of life sounds just fine</p>
