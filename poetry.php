<h2><a name="bday"></a>Happy B-Day</h2>
<p>"Happy Birthday" I sing to you<br/>
  Even though its words are few<br/>
  I hope that you will have a day<br/>
  Where you can sleep in and play</p>
<p>Everyone comes here to earth<br/>
  It's that miracle we call birth<br/>
  From that time on we celebrate<br/>
  Our birth event at a yearly rate</p>
<p>Birthdays can be a source of joy<br/>
  A time to yell "Wow!" and "Oh Boy!"<br/>
  It's your special day set aside<br/>
  So take the car for a joy ride</p>
<p>When your birthday comes around<br/>
  Don't be staring at the ground<br/>
  Get up, get out, and get away<br/>
  I hope you enjoy your happy day!</p>
