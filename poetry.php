<h2><a name="savior"></a>The Savior</h2>
<p>The Street was full of people<br/>
  A bright star in the sky<br/>
  People were looking heavenward<br/>
  A Savior was nearby</p>
<p>The boy was born of Mary<br/>
  And sent from God above<br/>
  To fulfill a mission<br/>
  And show us of His love</p>
<p>As life moved on He grew<br/>
  And always remembered the plan<br/>
  Given of His father<br/>
  He was no ordinary man</p>
<p>He taught the people boldly<br/>
  With authority from God<br/>
  The parables He'd given<br/>
  Would help out in His cause</p>
<p>The Jews rejected His message<br/>
  He knew that He would die<br/>
  Yet He still spake boldly<br/>
"I am that bread of life."</p>
<p>He suffered in a garden<br/>
  With apostles outside the gate<br/>
  The soldiers took Him captive<br/>
  And He died on Calvary</p>
<p>The heavens cried with thunder<br/>
  As rain fell to the earth<br/>
  Christ had done what was needed<br/>
  Ever since His birth</p>
<p>Three days passed with darkness<br/>
  A resurrection came<br/>
  Jesus fulfilled His mission<br/>
  So we can know His name</p>
