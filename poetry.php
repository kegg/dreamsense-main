<h2><a name="cat"></a>Ode To My Cat</h2>
<p>Oh little kitty<br/>
  how you must purr<br/>
  live life to it's fullest<br/>
  and not choosing to grrr</p>
<p>My precious sunshine<br/>
  why do you care<br/>
  to meow at shadows<br/>
  and laugh at thin air?</p>
<p>Light beams come and go<br/>
  through the world you see<br/>
  only staying for a brief moment<br/>
  then flying away like a bee</p>
<p>So little Kenya<br/>
  who's name has been changed<br/>
  to one called Maggie<br/>
  my you are strange</p>
<p>Poems about animals<br/>
  are meant as a gift<br/>
  not to mock or blame<br/>
  but rather to uplift</p>
<p>My little cat<br/>
  who sits on my lap<br/>
  falls asleep so easily<br/>
  and takes a long long nap</p>
