<p>Greg held the deck of cards out for the woman to witness. He shuffled
  through them time and time again. "Do you see your card?" He asked
  after pulling three cards from the deck.</p>

<p>The woman nodded. "Yes."</p>

<p>Greg carefully drew one of the cards. "Is this your card?"</p>

<p>The woman gasped and laughed. "Why yes, yes it is!"</p>

<p>He bowed and smiled. "You may return to your seat." Turning his attention
  to the clapping audience, Greg smiled. "I'm afraid that's all for tonight
  folks. I'm here all week."</p>

<p>Stepping behind the curtain, Greg set the deck of cdards down on a nearby
  table. He grinned from ear to ear as he walked towards his employer.</p>

<p>"Great work." The man wearing a top hat said. He was dressed to the nine's
  as it were.</p>

<p>Greg took off his jacket, still grinning. "Thank you sir." He wanted to
  know the reason his employer had showed up. Greg had been performing for
  well over a month and the man had never been to one of his performances
  until tonight.</p>

<p>"Greg," the man said. "What if I told you there was more to life than
  cards and tricks?"</p>

<p>Greg could feel his legs about to give way. He was getting fired for sure.
  Something didn't go right. His mind raced as he tried to figure out where
  the man was going with his question.</p>

<p>The man gestured to a chair. "Have a seat dear boy, have a seat."</p>

<p>Greg sat down. Sweat rolled down his face. His blue eyes staring at the
  employer who had his attention.</p>

<p>"My goodness man, you seem like you've seen a ghost. What's the matter?"</p>

<p>Greg shook his head. "Nothing sir, please continuye."</p>

<p>The man nodded. Grabbing a shot of whiskey he downed it. "In my day, an
  act like yours is what the house wanted. It was all anyone could do. It kept
  business going and butts in the seats." He paused, offering Greg a drink.</p>

<p>Greg declined the offer.</p>

<p>The man shrugged it off. "Nowadays, people want something more than just
  a simple magic show. They want... real magic."</p>

<p>Greg's mind raced. <em>Real magic</em>? What did the man mean by real magic!
  It had been years that Greg had been polishing his act. It was popular. It
  brought people in and kept people entertained. At least he thought it did.
  Now this man wanted "real magic".</p>

<p>"Real Magic," Gred repeated. "What does that even mean?"</p>

<p>The man smiled. "That's what I need you to figure out Greg." He turned and
  walked up a nearby staircase leading to street level. "That's what you need
  to find out."</p>

<p>With the door closing shut behind the man, Greg paced the floor a bit. He
  had an assignment. A task as it were. To find out what real magic was. Greg
  stopped pacing mid thought. What would happen to him if he didn't complete
  the task at hand?</p>

<p>Greg went home that night with what felt like the weight of the world on
  his shoulders. He needed to figure out an act, and figure it out quickly.</p>

<p>He didn't sleep much, tossed and turned a lot. About four am, Greg got out of
  bed and roamed the streets looking for something, anything to help him. What
  he found was little of much importance. He doubted a hooker would help him
  in his search. They seemed to be the only other creatures out that early in
  the morning.</p>

<p>Passing by an allyway, an old woman peeked her head out. "Young man" She
  said. Her voice was barely there. "Oh young man, can you help me?"</p>

<p>Greg stopped walking and looked her direction. "Yes ma'am?"</p>

<p>"Oh good, you can help me." She responded.</p>

<p>"What's wrong ma'am?"</p>

<p>My cat, she seems to have run off, have you seen her?</p>

<p>Greg shook his head. "No, I haven't seen a cat. What does it look like?"</p>

<p>The woman smiled. "Oh you'd know if you saw ol' Rusty. He's been through
  a few fights in his time."</p>

<p><em>Old Rusty</em>. Greg thought. What an odd name for a cat. He bowed his head
  and tipped his hat. "I'm sorry ma'am, I haven't seen any cats wandering about
  tonight, but I will keep an eye out for him."</p>

<p>The woman nodded. "Thank you young man. I best be getting home." She watched as
  Greg walked out of sight, and whispered to herself. "You'll find ol' Rusty I
  just know you will."</p>

<p>Arriving home, Greg went to bed. Perhaps sleep was what he needed. He was
  dead tired from walking around all night.</p>

<p>Later that morning, Greg woke up to a purring sound. Opening his eyes, he
  saw a cat. Oh it was a horrific looking animal. Missing an eye, part of an
  ear; and what he assumed was a partial tail. At least that's what it looked
  like to Greg. The cat was orange in color with white stripes.</p>

<p>"You wouldn't be ol' Rusty would you?" Greg asked half expecting the cat to
  answer.</p>

<p>The cat nodded its head as though it understood what Greg was saying.</p>

<p>Greg did a double take at the animal. "You understand me?"</p>

<p>The cat nodded again.</p<>

<p>"We best be getting you home to your owner. I think I know where the old
  woman lives."</p>

<p>The cat walked up the bed towards Greg's head. Putting a paw on his chest,
  it hissed at him.</p>

<p>Greg smiled. "Don't want to go home eh?" He paused. "I can see that."</p>

<p>Hopping out of bed, Greg went into the kitchen to get Rusty some milk.
  Putting a saucer down, the cat ran towards it and drank eagerly.</p>

<p>"Someone's thirsty." Greg said.</p>

<p>Stepping away from the saucer, the cat licked its paws.</p>

<p>A glow illuminated the cat. White then to yellow. The cat transformed ito
  a woman. A very naked woman.</p>

<p>Greg's smile dropped.</p>

<p>"Oh thank you sir." The woman said.</p>

<p>Turning away quickly, Greg shieled his eyes. "Um, miss, uh... Rusty?"</p>

<p>The woman stood from the floor smiling. "Well that's what the old bat calls
  me." She said. "My name is Elsa."</p>

<p>Greg exited the kitchen and brought back a towel. "Hurry, put this on miss."</p>

<p>Wrapping the towel around her body, the lady smiled. "Thank you Greg. It has
  been so long since I've been in my human form, I don't know what it feels like
  anymore. Being a cagt is weird. I'm always getting into fights.""</p>

<p>Greg nodded. "I could see that. Did that old woman change you into a cat?"</p>

<p>Elsa shook her head. "No, an old wizard did around two hundred years ago. The
  old woman foud me in an allyway begging for food and took me in. She won't let
  me have milk and doesn't know my true form."</p>

<p>Greg shook his head. "I don't koow what to say." He admitted. "Will the milk
  keep you a human?"</p>

<p>Elsa shook her head. "Nope, I'll be a cat again when the sun goes down."</p>

<p>"A shame."</p>

<p>Elsa ran her fingers through her long red hair. "Why is that? Don't you like
  cats?"</p>

<p>Greg smirked "It's not that. It's just, you deserve life to be more than
  being a cat."</p>

<p>"The wizard didn't think so." Elsa replied. "I was hungry one night, and I
  didn't have much to eat. I was living on the streets getting by here and there.
  Until I ran across the wizard." She explained. "He caught me steeling from his
  garden and punished me into cat form. To be a cat for eternity. I imagine he's
  long dead now. It <em>has been</em> 200 years."</p>

<p>Greg nodded. "You're welcome to stay with me." He said, as a light clicked on
  in his brain. Elsa could be the magic trick he was looking for. But to exploit
  her in such a way, as to garnish money for her curse? No, he thought, that
  would be cruel.</p>

<p>"Are you okay Greg?"</p>

<p>Greg paused. "How did you know my name is Greg?"</p>

<p>"There are papers with your name on them silly man."</p>

<p>Greg laughed. "Oh, yes that would make sense."</p>

<p>She looked around the kitchen, "and by the looks of things, you need help."</p>

<p>"What kind of wizard are you?" Elsa asked.</p>

<p>Greg shook his head. "I'm no wizard, I just play tricks here and there."</p>

<p>"I see." Elsa said. "Well, I could be your next big thing." She smiled. "Think
  about it. A woman that transforms into a cat and vicfe versa. I bet no one has
  seen that before."</p>

<p>Greg blinked. Was she actually offering herself to be part of his act? Why yes,
  yes she was.</p>

<p>"I would be a fool to turn you down."</p>

<p>Elsa smiled. "Then it's settled. When is your next performance?"</p>

<p>"Tonight." Greg said. "After sundown."</p>

<p>Elsa nodded her head. "Good, I't will give me a chance to buy some clothes
  for the occasion."</p>

<p>"I have some pants and a shirt you can wear until we find suitable
  clothing."</p>

<p>"Great!" Elsa said.</p>

<p>Elsa went shopping for some clothes while Greg practiced what he would say
  during his act. Over and over he rehearsed his lines until they became second
  nature to him.</p>

<p>As night approached, Elsa turned back into the cat.</p>

<p>Greg stepped out onto stage. "Ladies and Gentlemen, I have only one trick
  for you tonight. I am going to turn a cat into a human being."</p>

<p>He looked out as the crowd gasped and cheered. One man's gaze caught his
  attention. It was his employer. His employer was in the audience.</p>

<p>Greg smiled and nodded to the man. "This is going to be great." He muttered
  under his breath. "And now, for what you've been waiting for."</p>

<p>Elsa in cat form came out from behind the curtain.</p>

<p>Greg petted the cat and smiled. "A normal ally cat." He said.</p>

<p>Elsa purred.</p>

<p>Grabbing a container of milk, Greg pourd some into a bowl. "Now, with normal
  milk, we shall turn this cat into a human."</p>

<p>Placing a towel around the cat, Greg urged her to drink. Elsa drank the milk
  and turned back into a human.</p>

<p>The audience gasped and roared. They loved the trick. No one could explain
  how it occurred. They just loved it.</p>

<p>Greg helped Elsa up from the floor. "There you go ma'am." He said. "Now would
  you like to tell the audience of how you became a cat to begin with?"</p>

<p>Elsa nodded and spoke to the audience about her life as a human 200 years ago,
  how she became a cat and has been living on nine lives since. The audience ate
  it all up.</p>

<p>Greg looked to his employer who seemed to have vanished. He was no longer
  sitting in his chair. Maybe he went backstage, Greg thought.</p>

<p>As the house lights lowered, Greg hugged Elsa. "You were amazing!" He said
  grinning from ear to ear.</p>

<p>Elsa nodded. "Thanks."</p>

<p>Walking backstage, they ran into Greg's employer.</p>

<p>"There you are!" Greg exclaimed. "What did you think? Was that magical enough
  for you?"</p>

<p>His employer simply stared at Elsa. "I thought I had gotten rid of you."</p>

<p>Elsa did a double take. "No, it can't be." She said.</p>

<p>"Oh but it is. I am the wizard you stole food from."</p>

<p>Greg threw his hands up in the air. "Are you kidding me?" He said. "You're
  the wizard?" He sighed. "I suppose the old woman is working with you too?"</p>

<p>The wizard shook his head. "No, the old woman is just someone who happened
  upon Elsa, nothing more."</p>

<p>"I suppose you are going to punishg me more." Elsa said to the wizard.</p>

<p>The wizard shook his head. "No, I think 200 years has been enough. I release
  you fronm your curse." He said. "You are free."</p>

<p>Elsa smiled and turned to Greg. "I guess you'll have to find a different
  act."</p>

<p>Greg nodded. "Yes I suppose so."</p>

<p>The wizard smiled. "I will teach you some more tricks as you call them. I doubt
  you'll run out of any material anytime soon."</p>

<p>Greg looked to the wizard. "I'd like that."</p>

