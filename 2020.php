<h2>September</h2>

<h3>September 24, 2020</h3>

<p>Everything becomes a realism at some point or another. No one decides what you
  wish to believe or want to understand. That is up to you. To want to believe in
  something takes courage. To want to understand what that belief is? Even more
  courage. What it all takes is courage to see the truth in something no matter what.</p>
<p>There are times when we do not want to see the truth. We want to hide from it
  all. Kept safe in the world we created for ourselves. That peace of mind upon
  which we have the ability to stand.</p>
<p>So, what’s the point of it all if we know the truth and yet we cannot accept
  the truth for what it is? Makes you wonder what this life is all about sometimes.
  What point is any of it? Are there times when we wish the truth wouldn’t be known?
  Sure. But when millions believe in something, trust something, hold onto something
  they’ve been taught their whole life…where does that leave the truth? How does
  that lead them into a righteous cause? Doesn’t make sense does it.</p>
<p>What do you do with the knowledge that you retain. How much of that knowledge
  is good for you vs how much is bad. So many questions that occur and come up
  because you do not know left from right. Up from down. Life is but a limbo position
  constantly telling you where to go brining you down to some misery.</p>
<p>Either way, this life continues to drag people away from their current location
  and down into the depths of a sea. Never knowing where they will surface, if they
  will surface. At which point do you realize all that is going on and toss it aside
  as though it were nothing? Life is just that crazy.</p>
<p>Nah, thinking through things from time to time doesn’t fully make it all better.
   There are times which make these thoughts even worse. Something much different
   than how they are or will become. We all must find a way to become better than
    what we are at this present moment. Words on a page cannot do justice.</p>

